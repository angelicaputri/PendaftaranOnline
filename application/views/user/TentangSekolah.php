    <p>&nbsp &nbsp &nbsp &nbsp Sekolah adalah lembaga untuk para siswa pengajaran siswa/murid di bawah pengawasan guru.

Sebagian besar negara memiliki sistem pendidikan formal yang umumnya wajib. Dalam sistem ini, siswa mengalami kemajuan melalui serangkaian kegiatan belajar mengajar di sekolah. Nama-nama untuk sekolah ini bervariasi menurut negara (dibahas pada bagian Daerah di bawah), tetapi umumnya termasuk sekolah dasar untuk anak-anak muda dan sekolah menengah untuk remaja yang telah menyelesaikan pendidikan dasar.</p>

<p>&nbsp &nbsp &nbsp &nbsp Selain sekolah inti, siswa di negara tertentu juga mungkin memiliki akses dan mengikuti sekolah baik sebelum dan sesudah pendidikan dasar dan menengah. TK atau pra-sekolah menyediakan sekolah bagi anak-anak (biasanya umur 3-5 tahun). Universitas, sekolah kejuruan, perguruan tinggi atau seminari mungkin tersedia setelah sekolah menengah. Sebuah sekolah mungkin juga didedikasikan untuk satu bidang tertentu, seperti sekolah ekonomi atau sekolah tari. Alternatif sekolah dapat menyediakan kurikulum dan metode non-tradisional.</p>

<p>&nbsp &nbsp &nbsp &nbsp Ada juga sekolah non-pemerintah, yang disebut sekolah swasta. Sekolah swasta mungkin untuk anak-anak dengan kebutuhan khusus ketika pemerintah tidak bisa memberi sekolah khusus bagi mereka; keagamaan, seperti sekolah Islam, sekolah Kristen, hawzas, yeshivas dan lain-lain, atau sekolah yang memiliki standar pendidikan yang lebih tinggi atau berusaha untuk mengembangkan prestasi pribadi lainnya. Sekolah untuk orang dewasa meliputi lembaga-lembaga pelatihan perusahaan dan pendidikan dan pelatihan militer.</p>

<p>&nbsp &nbsp &nbsp &nbsp Kata sekolah berasal dari Bahasa Latin: skhole, scola, scolae atau skhola yang memiliki arti: waktu luang atau waktu senggang, di mana ketika itu sekolah adalah kegiatan di waktu luang bagi anak-anak di tengah-tengah kegiatan utama mereka, yaitu bermain dan menghabiskan waktu untuk menikmati masa anak-anak dan remaja. Kegiatan dalam waktu luang itu adalah mempelajari cara berhitung, cara membaca huruf dan mengenal tentang moral (budi pekerti) dan estetika (seni). Untuk mendampingi dalam kegiatan scola anak-anak didampingi oleh orang ahli dan mengerti tentang psikologi anak, sehingga memberikan kesempatan yang sebesar-besarnya kepada anak untuk menciptakan sendiri dunianya melalui berbagai pelajaran di atas.</p>

<p>&nbsp &nbsp &nbsp &nbsp Saat ini, kata sekolah berubah arti menjadi: merupakan bangunan atau lembaga untuk belajar dan mengajar serta tempat menerima dan memberi pelajaran.Sekolah dipimpin oleh seorang Kepala Sekolah. Kepala sekolah dibantu oleh wakil kepala sekolah.Jumlah wakil kepala sekolah di setiap sekolah berbeda, tergantung dengan kebutuhannya. Bangunan sekolah disusun meninggi untuk memanfaatkan tanah yang tersedia dan dapat diisi dengan fasilitas yang lain. Ketersediaan sarana dalam suatu sekolah mempunyai peran penting dalam terlaksananya proses pendidikan.</p>
